public class FacePattern {
	public static void main(String[] args) {
		System.out.println();
		for (int y = 1; y <= 5; y++) {
			if (y == 1) System.out.println("  +\" \" \" \" \"+  ");	
			if (y == 2) System.out.println("[ |  o   o  | ]");
			if (y == 3) System.out.println("  |    ^    | ");
			if (y == 4) System.out.println("  |  ' - '  | ");
			if (y == 5) System.out.println("  +- - - - -+ ");
		}
	}
}